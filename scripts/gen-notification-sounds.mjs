// Generates the in-app activity notification chime (Stream 1) as a self-contained
// base64 WAV data URI, written to src/renderer/assets/activityChime.ts.
//
// Embedding as a data URI (rather than a bundled .wav asset) keeps it working under the
// Tauri packaged app's asset protocol + CSP with zero webpack/loader changes, and needs
// no TS module declaration. Re-run after tweaking the tone; commit the generated .ts.
//
//   node scripts/gen-notification-sounds.mjs

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const SAMPLE_RATE = 44100;

// A gentle two-note "ding-dong" chime: a perfect fourth (E6 → A6), each note a sine
// with a fast attack and exponential decay. Subtle, short, non-jarring.
function renderChime() {
  const notes = [
    { freq: 1318.51, start: 0.0, dur: 0.18 }, // E6
    { freq: 1760.0, start: 0.11, dur: 0.26 }, // A6 (overlaps slightly)
  ];
  const totalDur = 0.42;
  const n = Math.floor(totalDur * SAMPLE_RATE);
  const samples = new Float32Array(n);
  const amp = 0.22; // conservative headroom; the app scales volume further at playback
  for (const note of notes) {
    const startSample = Math.floor(note.start * SAMPLE_RATE);
    const noteSamples = Math.floor(note.dur * SAMPLE_RATE);
    for (let i = 0; i < noteSamples; i++) {
      const idx = startSample + i;
      if (idx >= n) break;
      const t = i / SAMPLE_RATE;
      const env = Math.exp(-t * 14); // fast decay
      const attack = Math.min(1, i / (0.004 * SAMPLE_RATE)); // 4ms attack, avoids a click
      samples[idx] += amp * attack * env * Math.sin(2 * Math.PI * note.freq * t);
    }
  }
  return samples;
}

function toWavBuffer(samples) {
  const bytesPerSample = 2; // 16-bit PCM
  const dataSize = samples.length * bytesPerSample;
  const buf = Buffer.alloc(44 + dataSize);
  buf.write('RIFF', 0);
  buf.writeUInt32LE(36 + dataSize, 4);
  buf.write('WAVE', 8);
  buf.write('fmt ', 12);
  buf.writeUInt32LE(16, 16); // PCM chunk size
  buf.writeUInt16LE(1, 20); // audio format = PCM
  buf.writeUInt16LE(1, 22); // channels = mono
  buf.writeUInt32LE(SAMPLE_RATE, 24);
  buf.writeUInt32LE(SAMPLE_RATE * bytesPerSample, 28); // byte rate
  buf.writeUInt16LE(bytesPerSample, 32); // block align
  buf.writeUInt16LE(16, 34); // bits per sample
  buf.write('data', 36);
  buf.writeUInt32LE(dataSize, 40);
  let off = 44;
  for (let i = 0; i < samples.length; i++) {
    const s = Math.max(-1, Math.min(1, samples[i]));
    buf.writeInt16LE(Math.round(s * 32767), off);
    off += 2;
  }
  return buf;
}

const wav = toWavBuffer(renderChime());
const dataUri = `data:audio/wav;base64,${wav.toString('base64')}`;

const out = `// AUTO-GENERATED by scripts/gen-notification-sounds.mjs — do not edit by hand.
// The in-app activity notification chime, embedded as a base64 WAV data URI so it
// works under the Tauri packaged app / CSP with no bundler asset handling.
export const ACTIVITY_CHIME_DATA_URI =
  '${dataUri}';
`;

const target = resolve(dirname(fileURLToPath(import.meta.url)), '../src/renderer/assets/activityChime.ts');
writeFileSync(target, out);
console.log(`Wrote ${target} (${wav.length} bytes WAV, ${dataUri.length} chars data URI)`);
